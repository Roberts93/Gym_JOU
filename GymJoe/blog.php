<p>1- Pastas pre cocidas
Estas pastas ya vienen cocidas y para poder servirlas en la mesa solo tienes que calentarlas en el microondas por lo que no te llevara más de 5 minutos.
Puedes condimentarlas con algunas especies ligeras y tendrás un menú exquisito, muy saludable y preparado en minutos.</p>

<p>2- Pescado o pollo asado
Para que sea saludable debe tratarse de algún tipo de pescado no graso o bien, alguna parte del pollo como 
la pechuga que sea solo carne y no grasas. Puedes cortarlos finos y ponerlos a asar a la parrilla, mientras preparas
una deliciosa ensalada para acompañar la carne.</p>

<p>3- Ensaladas
Si quieres una comida verdaderamente saludable y muy rápida, entonces opta por las ensaladas. Hay miles de ensaladas diferentes por lo que puedes ir variando cada día para no caer en el aburrimiento.
Recuerda que es muy importante lavar correctamente las verduras y condimentarla, en lo posible, usando aceite de oliva y limón.
Comienza a buscar recetas de ensaladas en Internet y prepara lo que más te gusta, puedes tardar 10 minutos.</p>

<p>4- Carne magra y vegetales asados
Asar a la parrilla es uno de los métodos de cocción más saludables y además, rápido. Por lo que si buscas trozos 
de carne que no contengan demasiada grasas y algunos vegetales que te gustan, puedes asarlos en la parrilla y preparar 
un exquisito plato.</p>


<p>5- Medallones de atún asados
El atún es un alimento muy saludable, es rico en proteínas y bajo en grasas por lo que es cocerlo a la parrilla junto a
algunos vegetales es una idea perfecta que podrás preparar en cuestión de minutos.</p>

