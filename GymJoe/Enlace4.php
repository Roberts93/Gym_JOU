  <p>
Las lagartijas son ejercicios de flexión que permiten trabajar y fortalercer los músculos del tren superior, especialmente pectorales, hombros, trícpes, y deltoides, entre otros músculos, como los abdominales y la espalda.
    </p>
<p>
  De acuerdo con los entrenadores de trabajo físico, uno de los ejercicios más eficaces para tonificar y fortalecer los brazos, son las lagartijas.
  
Éste es el trabajo más completo para la parte superior del cuerpo. Además de fortalecer el pecho, hombros y tríceps, ejercita tus abdominales, glúteos, así como la espalda alta y baja del cuerpo.
</p>

<p>
Para realizar las lagartijas colóquese boca abajo sobre el piso, acuéstese y apóyese sobre los puños, con los brazos doblados. Después, coloque la punta de los pies sobre el suelo y manténga su cuerpo en línea recta durante todo el movimiento. Recuerde mantener su estómago contraído al igual que los músculos de los glúteos.
 
De esta forma, logrará reducir la flacidez de sus brazos, lucir unos hombros firmes y en consecuencia usar la ropa que más le gusta. ¡Haga la prueba!
</p>
